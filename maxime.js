function loadWasteSection() {
  var ingreCat = document.getElementById("ingreCat"),
    ingredientName = document.getElementById("ingredientName"),
    citySel = document.getElementById("citySel");
  for (var state in stateObject1) {
    ingreCat.options[ingreCat.options.length] = new Option(state, state);
  }
  ingreCat.onchange = function() {
    ingredientName.length = 1; // remove all options bar first
    citySel.length = 1; // remove all options bar first
    if (this.selectedIndex < 1) return; // done
    for (var county in stateObject1[this.value]) {
      ingredientName.options[ingredientName.options.length] = new Option(
        county,
        county
      );
    }
  };
  ingreCat.onchange(); // reset in case page is reloaded
  ingredientName.onchange = function() {
    citySel.length = 1; // remove all options bar first
    if (this.selectedIndex < 1) return; // done
    var cities = stateObject1[ingreCat.value][this.value];
    for (var i = 0; i < cities.length; i++) {
      citySel.options[citySel.options.length] = new Option(
        cities[i],
        cities[i]
      );
      document.getElementById("productCategory").innerHTML = cities[0];
      document.getElementById("bins").innerHTML = cities[1];
    }
  };
}

window.onload = function() {
  loadWasteSection();
};

